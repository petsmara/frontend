<template>
  <div class="register">
    <h2 class="register__title">
      회원가입
    </h2>
    <el-form
      :model="ruleForm"
      :rules="rules"
      ref="ruleForm"
      class="register__form"
      status-icon
    >
      <el-form-item label="이메일" prop="email">
        <el-input type="email" v-model="ruleForm.email"></el-input>
      </el-form-item>
      <el-form-item label="닉네임" prop="nickname">
        <el-input v-model="ruleForm.nickname"></el-input>
      </el-form-item>
      <el-form-item label="핸드폰번호" prop="phone">
        <el-input v-model="ruleForm.phone"></el-input>
      </el-form-item>
      <el-form-item label="비밀번호" prop="password">
        <el-input
          type="password"
          v-model="ruleForm.password"
          autocomplete="off"
        ></el-input>
      </el-form-item>
      <el-form-item label="비밀번호 확인" prop="checkPassword">
        <el-input
          type="password"
          v-model="ruleForm.checkPassword"
          autocomplete="off"
        ></el-input>
      </el-form-item>
      <el-form-item class="register__submit">
        <el-button
          :loading="isLoading"
          type="primary"
          @click="submitForm('ruleForm')"
          >가입하기</el-button
        >
        <!-- <el-button @click="resetForm('ruleForm')">모두 지우기</el-button> -->
      </el-form-item>
    </el-form>
  </div>
</template>

<script>
import axios from 'axios'
import { createNamespacedHelpers } from 'vuex'
const { mapState, mapActions } = createNamespacedHelpers('user')

export default {
  data() {
    const validateNickname = (rule, value, callback) => {
      const checkNickname = /^[\w\Wㄱ-ㅎㅏ-ㅣ가-힣]/
      const checkWhiteSpace = /\s/

      if (!checkNickname.test(value) || checkWhiteSpace.test(value)) {
        callback(new Error('형식에 맞지 않습니다.'))
      } else {
        callback()
      }
    }
    const validateMobileNumber = (rule, value, callback) => {
      const checkMobileNumber = /^01(?:0|1|[6-9])(?:\d{3}|\d{4})\d{4}$/
      if (!checkMobileNumber.test(value)) {
        callback(new Error('형식에 맞지 않는 번호입니다.'))
      } else {
        callback()
      }
    }
    const validatePass = (rule, value, callback) => {
      if (value === '') {
        callback(new Error('비밀번호를 입력해주세요.'))
      } else {
        if (this.ruleForm.checkPassword !== '') {
          this.$refs.ruleForm.validateField('checkPassword')
        }
        callback()
      }
    }
    const validatePass2 = (rule, value, callback) => {
      if (value === '') {
        callback(new Error('한번 더 비밀번호를 입력해주세요.'))
      } else if (value !== this.ruleForm.password) {
        callback(new Error('비밀번호가 일치하지 않습니다.'))
      } else {
        callback()
      }
    }
    return {
      isLoading: false,
      ruleForm: {
        email: '',
        nickname: '',
        phone: '',
        password: '',
        checkPassword: ''
      },
      rules: {
        email: [
          {
            required: true,
            message: '이메일을 입력해주세요.',
            trigger: 'blur'
          },
          {
            type: 'email',
            message: '이메일을 양식에 맞춰 입력해주세요.',
            trigger: ['blur', 'change']
          }
        ],
        nickname: [
          {
            validator: validateNickname,
            message: '형식에 맞지 않습니다.',
            trigger: ['change']
          },
          {
            min: 2,
            max: 20,
            message: '2~20 사이 문자를 입력해주세요',
            trigger: ['blur']
          }
        ],
        phone: [
          {
            required: true,
            message: '핸드폰 번호를 입력해주세요.',
            trigger: 'blur'
          },
          {
            validator: validateMobileNumber,
            trigger: ['blur', 'change']
          }
        ],
        password: [
          {
            required: true,
            message: '비밀번호를 입력해주세요.',
            trigger: 'blur'
          },
          {
            min: 8,
            max: 20,
            trigger: 'blur',
            message: '8~20자로 비밀번호를 입력해주세요.'
          },
          { validator: validatePass, trigger: 'blur' }
        ],
        checkPassword: [
          {
            required: true,
            message: '비밀번호를 입력해주세요.',
            trigger: 'blur'
          },
          { validator: validatePass2, trigger: 'blur' }
        ]
      }
    }
  },
  methods: {
    ...mapActions(['registerUser']),
    submitForm(formName) {
      this.isLoading = true
      const { email, nickname, password, phone: phone_number } = this.ruleForm
      this.$refs[formName].validate(valid => {
        if (valid) {
          alert('회원가입이 완료되었습니다.!')
          this.registerUser({
            email,
            nickname,
            password,
            phone_number
          })
        } else {
          console.log('회원가입이 실패하였습니다.!!')
        }
        this.isLoading = false
      })
    }
  }
}
</script>

